function animate() {
  window.requestAnimationFrame(animate)
  c.fillStyle = 'black'
  c.fillRect(0, 0, canvas.width, canvas.height)
  background.update()
  shop.update()
  c.fillStyle = 'rgba(255, 255, 255, 0.15)'
  c.fillRect(0, 0, canvas.width, canvas.height)
  player.update()
  enemy.update()

  player.velocity.x = 0
  enemy.velocity.x = 0

  // player movement

  if (keys.a.pressed && player.lastKey === 'a') {
    player.velocity.x = -5
    player.switchSprite('run')
  } else if (keys.d.pressed && player.lastKey === 'd') {
    player.velocity.x = 5
    player.switchSprite('run')
  } else {
    player.switchSprite('idle')
  }

  // Check if player is out of bounds
  if (player.position.x < 0) {
    player.position.x = 0;
  }
  if (player.position.x + player.width > canvas.width) {
    player.position.x = canvas.width - player.width;
  }

  // jumping
  if (player.velocity.y < 0) {
    player.switchSprite('jump')
  } else if (player.velocity.y > 0) {
    player.switchSprite('fall')
  }

  // Enemy movement
  if (keys.ArrowLeft.pressed && enemy.lastKey === 'ArrowLeft') {
    enemy.velocity.x = -5
    enemy.switchSprite('run')
  } else if (keys.ArrowRight.pressed && enemy.lastKey === 'ArrowRight') {
    enemy.velocity.x = 5
    enemy.switchSprite('run')
  } else {
    enemy.switchSprite('idle')
  }

  if (enemy.position.x < 0){
    enemy.position.x = 0;
  }
  if (enemy.position.x + enemy.width > canvas.width){
    enemy.position.x = canvas.width - enemy.width;
  }

  // jumping
  if (enemy.velocity.y < 0) {
    enemy.switchSprite('jump')
  } else if (enemy.velocity.y > 0) {
    enemy.switchSprite('fall')
  }

  // detect for collision & enemy gets hit
  if (
    rectangularCollision({
      rectangle1: player,
      rectangle2: enemy
    }) &&
    player.isAttacking &&
    player.framesCurrent === 4
  ) {
    enemy.takeHit()
    player.isAttacking = false

    gsap.to('#enemyHealth', {
      width: enemy.health + '%'
    })
  }

  // if player misses
  if (player.isAttacking && player.framesCurrent === 4) {
    player.isAttacking = false
  }

  // this is where our player gets hit
  if (
    rectangularCollision({
      rectangle1: enemy,
      rectangle2: player
    }) &&
    enemy.isAttacking &&
    enemy.framesCurrent === 2
  ) {
    player.takeHit()
    enemy.isAttacking = false

    gsap.to('#playerHealth', {
      width: player.health + '%'
    })
  }

  // if player misses
  if (enemy.isAttacking && enemy.framesCurrent === 2) {
    enemy.isAttacking = false
  }

  // end game based on health
  if (enemy.health <= 0 || player.health <= 0) {
    determineWinner({ player, enemy, timerId })

    // Wait for 5 seconds before showing the results and asking to play again
    setTimeout(function() {
      // Determine the winner
      let winner;
      if (player.health <= 0 && enemy.health <= 0) {
        winner = 'It\'s a draw';
        alert(winner);
        // Both players get 50 points in case of a draw
        playerScore += 50;
        enemyScore += 50;
      } else {
        winner = player.health > 0 ? 'Player' : 'Enemy';
        // Show the results
        alert(winner + ' won the game!');
        if (winner === 'Player') {
          playerScore += 100;
        } else {
          enemyScore += 100;
        }
      }
      
      if(playerScore >= 2000 || enemyScore >= 2000){
        alert("Game over. You have reached the maximum score of 2000");
        localStorage.removeItem('playerScore');
        localStorage.removeItem('enemyScore');
      } else {
        // Збережіть оновлені очки у localStorage
        localStorage.setItem('playerScore', playerScore);
        localStorage.setItem('enemyScore', enemyScore);
      }

      // Ask the user if they want to play again
      let playAgain = window.confirm("Game over. Do you want to play again?");
      if (playAgain) {
        window.location.reload();
      }
    }, 1200);
  }
}

